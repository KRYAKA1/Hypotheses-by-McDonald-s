print("Скорее всего потому что у коровы и свиньи едят больше еды содержащие белки или их организм больше их вырабатывают")



feg = pd.Series(data = [j1,y1,j2,y2,j3,y3,j4,y4], index = ["Калорий говядине и свинине","Калорий в рыбе и курице","Жири в говядине и свинине","Жири в рыбе и курице","Углеводы в говядине и свинине","Углеводы в рыбе и курице","Белки в говядине и свинине","Белки в рыбе и курице"])

feg.plot(kind = 'barh', title= "Среднее количество калорий, жиров, углеводов, белков",figsize = (20, 7), grid = True)


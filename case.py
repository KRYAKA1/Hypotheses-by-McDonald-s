import pandas as pd 
import matplotlib.pyplot as plt

df = pd.read_csv("menu.csv")

j1 = df[df['Category'] == "Beef & Pork"]["Calories"].mean()
y1 = df[df['Category'] == "Chicken & Fish"]["Calories"].mean()
j2 = df[df['Category'] == "Beef & Pork"]["Total Fat"].mean()
y2 = df[df['Category'] == "Chicken & Fish"]["Total Fat"].mean()
j3 = df[df['Category'] == "Beef & Pork"]["Carbohydrates"].mean()
y3 = df[df['Category'] == "Chicken & Fish"]["Carbohydrates"].mean()
j4 = df[df['Category'] == "Beef & Pork"]["Protein"].mean()
y4 = df[df['Category'] == "Chicken & Fish"]["Protein"].mean() 

print("Проверим гипотезу что более каларийное курица с рыбой или говядина с свининой")
print("Проверка подсчитала что в курице и рыбе:", round(j1, 2),"калорий, а в говядине и свинине:", round(y1, 1))
print("Возможно это из за того что большинство блюд с говядиной и свининой делают во фритюре")
print( )
print("Проверим гипотезу в чем больше жиров в курице с рыбой или говядине с свининой")
print("Проверка подсчитала что в курице и рыбе:", round(j2, 1),"жиров, а в говядине и свинине:", round(y2, 1))
print("Скорее всего потому что у коровы и свиньи более жирное мясо или из за того что зимой корове и свинье нужен жир чтобы согреватся а вот курицы есть перья, а рыба живет в воде")
print( )
print("Проверим гипотезу в чем больше углеводов в курице с рыбой или говядине с свининой")
print("Проверка подсчитала что в курице и рыбе:", round(j3, 1),"углеводов, а в говядине и свинине:", round(y3, 1))
print("Углеводы берутся благодаря фотосинтезу в растениях, похоже свиньи и коровы едят много травы")
print( )
print("Проверим гипотезу в чем больше белков в курице с рыбой или говядине с свининой") 
